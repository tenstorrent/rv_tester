def create_cpp_frag(core_events: List[Dict[Any, Any]],sc_events: List[Dict[Any, Any]], path="gen_events.hpp"):
    events = core_events + sc_events
    tab = "      "
    f = open(path, "w")
    enum_pre = """//AUTOGENERATED -- NO TOUCH

    typedef enum : size_t {"""
    f.write(enum_pre)
    f.write("\n")
    enum_post = """    COUNT
    } counter;"""
    for event in events:
        name = event["name"]
        description = event["description"]
        f.write(f"      //{description}")
        f.write("\n")
        f.write(f"      {name.upper()},")
        f.write("\n")
    f.write(enum_post)
    to_vec_pre = f"""
    void to_vector(const rv_tester_transactions::pmu::pmcounters<>& pmcounters){{
    
      const uint64_t casting_size_term = uint64_t(1) << {pmcounter_dpi_width};

"""
    to_vec_post = """
    }

    """
    f.write('\n\n')
    f.write(to_vec_pre)
    for event in events:
        name = event["name"]
        f.write(f"{tab}counters[counter::{name.upper()}] = counters[counter::{name.upper()}] + ((pmcounters.{name} - (counters[counter::{name.upper()}] % casting_size_term)) % casting_size_term);")
        f.write("\n")
    f.write(to_vec_post)
    
    reflection_pre = """
    const std::unordered_map<counter, std::string_view> to_string = {
"""
    f.write(reflection_pre)
    for event in events:
        name = event["name"]
        f.write(f'{tab}{{{name.upper()},"{name}"}},')
        f.write('\n')
    f.write('    };')

    event_map_pre = """

    const std::unordered_map<uint64_t, size_t> event_map = {
"""
    f.write(event_map_pre)
    for event in core_events:
        name = event["name"]
        event_id = event["event_id"]
        f.write(f'{tab}{{{event_id},counter::{name.upper()}}},')
        f.write('\n')
    f.write('    };')

    filtered_event_map_pre = """

    const std::unordered_map<uint64_t, std::unordered_map<uint16_t, size_t>> filtered_event_map = {
"""
    f.write(filtered_event_map_pre)
    filtered_csv = []
    filter_event_dict = {}
    for event in core_events:
        if (event["multi_hot_encoding"] == "Yes" and event["multi_D_filter"] == "No"):
            filtered_csv.append({
                'event_31_16': event["event_id"][0:6],
                'event_15_0': event["event_id"][6:10],
                'name': event['name'],
            })
    for row in filtered_csv:
        parent = row['event_31_16']
        if parent not in filter_event_dict:
            filter_event_dict[parent] = {}
        filter_event_dict[parent][row['event_15_0']] = row['name']
        
    for parent, child_dict in filter_event_dict.items():
        filter_map_str = f"{tab}{{{parent}," + "{"
        for child, event_name in child_dict.items():
            bit_position = "0x"+child if child != '0' else None
            filter_map_str += f'\n{tab}{tab}{{{bit_position},counter::{event_name.upper()}}},'
        filter_map_str += "\n"+ f'{tab}{tab}'+"}\n"+f'{tab}'+"},\n"
        f.write(filter_map_str)
    f.write(f'    '+"};")
    filtered_event_map_post="""

//AUTOGENERATED -- END
"""
    f.write(filtered_event_map_post)


def create_sv_frag(events: List[Dict[Any, Any]], path="gen_events.sv", prefix="", skip_pre = False):
    tab = "      "
    ignore_names = ["cpu_cycles", "instructions", "branch_instructions"]

    f = open(path, 'w')
    pre = f"""
    //AUTOGENERATED -- NO TOUCH
    localparam MAX_COUNTER_VALUE_CHANGE_IN_ONE_CYCLE = 32;
    parameter OVERFLOW_BIT = {pmcounter_dpi_width} - 1;
    if (OVERFLOW_BIT < ($clog2(MAX_COUNTER_VALUE_CHANGE_IN_ONE_CYCLE) + 1)) $error("The selected overflow bit cannot cover the maximum value change of a counter within a single clock cycle.");
    parameter OVERFLOW_BIT_EXTRA = 2;
    logic overflow;
    logic [EVENT_COUNT + SC_EVENT_COUNT + OVERFLOW_BIT_EXTRA -1 : 0] pmcounter_overflow_bit;
    assign pmcounterss[0].valid = !reset && perf_enabled && (overflow || (|mhpm_write) || terminate || (cycle_sync_en && (sync_cycles % period) == 0) || (instruction_sync_en && (((prev_sync_instructions % instructions) > nret) && ((sync_instructions % instructions) < nret))) || perf_start || perf_end);
    assign pmcounterss[0].data.location = location;
    assign pmcounterss[0].data.cpu_cycles = {pmcounter_dpi_width}'(cpu_cycles);
    assign pmcounterss[0].data.instructions = {pmcounter_dpi_width}'(pmcounter[INSTRUCTIONS]);
    assign pmcounterss[0].data.branch_instructions = {pmcounter_dpi_width}'(branch_instructions);
    assign pmcounterss[0].data.perf_start = perf_start;
    assign pmcounterss[0].data.perf_end = perf_end;
    assign pmcounterss[0].data.terminate = terminate;
    assign pmcounterss[0].data.overflow = overflow;
    assign pmcounterss[0].data.sync = (cycle_sync_en && (sync_cycles % period) == 0) || (instruction_sync_en && (((prev_sync_instructions % instructions) > nret) && ((sync_instructions % instructions) < nret)));
"""
    if not skip_pre:
        f.write(pre)
    for event in events:
        name = event["name"]
        if name in ignore_names:
            continue
        if prefix == "":
            f.write(
                f"    assign pmcounterss[0].data.{name} = {pmcounter_dpi_width}'({prefix}pmcounter[{name.upper()}]);")
        elif prefix == "sc_":
            f.write(
                f"    assign pmcounterss[0].data.{name} = {pmcounter_dpi_width}'({prefix}pmci[{name.upper()}]);")
        f.write('\n')


def create_defines_frag(events: List[Dict[Any, Any]], prefix="", path="gen_defines.sv"):
    prelude = "    //AUTOGENERATED -- NOTOUCH \n    typedef enum {\n"
    poslude = f"""        {prefix.upper()}EVENT_COUNT
    }} {prefix}pmc_event_t;"""

    f = open(path, 'w')
    f.write(prelude)
    for event in events:
        name = event["name"]
        f.write(f"        {name.upper()},\n")
    f.write(poslude)


def create_yaml_frag(events: List[Dict[Any, Any]], path="gen_events.yaml", mod="", skip_prelude = False):
    tab = "    "
    f = open(path, 'w')
    if not skip_prelude:
        prelude = f"  #AUTOGENERATED -- NOTOUCH \n{tab*2}fields:\n"
        f.write(prelude)

    for event in events:
        name = event["name"]
        f.write(f"{tab * 3}{name}:\n")
        if mod == "": 
            f.write(f"{tab * 4}width: {pmcounter_dpi_width}\n")
        elif mod == "sc": 
            f.write(f"{tab * 4}width: {pmcounter_dpi_width}\n")

    if skip_prelude:
        poslude ="""            perf_start:
                width: 1
            perf_end:
                width: 1
            terminate:
                width: 1
            overflow:
                width: 1
            sync:
                width: 1"""
        f.write(poslude)

def create_monitor_frag(events: List[Dict[Any, Any]], prefix="", path="gen_monitor.sv"):
    prelude = """
    always_comb begin
    """
    poslude = """
    end
    """
    f = open(path, 'w')
    f.write(prelude)
    for event in events:
        name = event["name"]
        f.write(f"        {prefix}pmci[{name.upper()}] = {name};\n")
    f.write(poslude)

