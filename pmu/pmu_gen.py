def create_cpp_frag(events: List[Dict[Any, Any]], path="gen_events.hpp"):
    tab = "      "
    f = open(path, "w")
    enum_pre = """
    //AUTOGENERATED -- NO TOUCH

    typedef enum : size_t {"""
    f.write(enum_pre)
    f.write("\n")
    enum_post = """    COUNT
    } counter;"""
    for event in events:
        name = event["name"]
        description = event["description"]
        f.write(f"      //{description}")
        f.write("\n")
        f.write(f"      {name.upper()},")
        f.write("\n")
    f.write(enum_post)
    to_vec_pre = """
    //AUTOGENERATED -- NO TOUCH

std::vector<uint64_t> to_vector(const rv_tester_transactions::pmu::pmcounters<>& pmcounters)
    {
      std::vector<uint64_t> tmp(counter::COUNT);

      """
    to_vec_post = """
      return tmp;
    }

    """
    f.write('\n\n')
    f.write(to_vec_pre)
    for event in events:
        name = event["name"]
        f.write(f"{tab}tmp[counter::{name.upper()}] = pmcounters.{name};")
        f.write("\n")
    f.write(to_vec_post)

    reflection_pre = """
    const std::unordered_map<counter, std::string_view> to_string =
      {
"""
    f.write(reflection_pre)
    for event in events:
        name = event["name"]
        f.write(f'{tab}{{{name.upper()},"{name}"}},')
        f.write('\n')
    f.write('    };')


def create_sv_frag(events: List[Dict[Any, Any]], path="gen_events.sv", prefix="", skip_pre = False):
    tab = "      "
    ignore_names = ["cpu_cycles", "instructions", "branch_instructions"]

    f = open(path, 'w')
    pre = f"""
    //AUTOGENERATED -- NO TOUCH
    assign pmcounterss[0].valid = !reset && perf_enabled && (terminate || (cycle_sync_en && (sync_cycles % period) == 0) || (instruction_sync_en && (((prev_sync_instructions % instructions) > nret) && ((sync_instructions % instructions) < nret))) || perf_start || perf_end);
    assign pmcounterss[0].data.location = location;
    assign pmcounterss[0].data.cpu_cycles = {pmcounter_width}'(cpu_cycles);
    assign pmcounterss[0].data.instructions = {pmcounter_width}'(pmcounter[INSTRUCTIONS]);
    assign pmcounterss[0].data.branch_instructions = {pmcounter_width}'(branch_instructions);
    assign pmcounterss[0].data.perf_start = perf_start;
    assign pmcounterss[0].data.perf_end = perf_end;
    assign pmcounterss[0].data.terminate = terminate;
    assign pmcounterss[0].data.sync = (cycle_sync_en && (sync_cycles % period) == 0) || (instruction_sync_en && (((prev_sync_instructions % instructions) > nret) && ((sync_instructions % instructions) < nret)));
"""
    if not skip_pre:
        f.write(pre)
    for event in events:
        name = event["name"]
        if name in ignore_names:
            continue
        if prefix == "":
            f.write(
                f"    assign pmcounterss[0].data.{name} = {pmcounter_width}'({prefix}pmcounter[{name.upper()}]);")
        elif prefix == "sc_":
            f.write(
                f"    assign pmcounterss[0].data.{name} = {pmci_width}'({prefix}pmci[{name.upper()}]);")
        f.write('\n')


def create_defines_frag(events: List[Dict[Any, Any]], prefix="", path="gen_defines.sv"):
    prelude = "    //AUTOGENERATED -- NOTOUCH \n    typedef enum {\n"
    poslude = f"""        {prefix.upper()}EVENT_COUNT
    }} {prefix}pmc_event_t;"""

    f = open(path, 'w')
    f.write(prelude)
    for event in events:
        name = event["name"]
        f.write(f"        {name.upper()},\n")
    f.write(poslude)


def create_yaml_frag(events: List[Dict[Any, Any]], path="gen_events.yaml", mod="", skip_prelude = False):
    tab = "    "
    f = open(path, 'w')
    if not skip_prelude:
        prelude = f"  #AUTOGENERATED -- NOTOUCH \n{tab*2}fields:\n"
        f.write(prelude)

    for event in events:
        name = event["name"]
        f.write(f"{tab * 3}{name}:\n")
        if mod == "": 
            f.write(f"{tab * 4}width: {pmcounter_width}\n")
        elif mod == "sc": 
            f.write(f"{tab * 4}width: {pmci_width}\n")

    if skip_prelude:
        poslude ="""            perf_start:
                width: 1
            perf_end:
                width: 1
            terminate:
                width: 1
            sync:
                width: 1"""
        f.write(poslude)

def create_monitor_frag(events: List[Dict[Any, Any]], prefix="", path="gen_monitor.sv"):
    prelude = """
    always_comb begin
    """
    poslude = """
    end
    """
    f = open(path, 'w')
    f.write(prelude)
    for event in events:
        name = event["name"]
        f.write(f"        {prefix}pmci[{name.upper()}] = {name};\n")
    f.write(poslude)

